#include <stdio.h>

int main()
{
    int a, b, sum;

    printf("Enter the 1st number : \n");
    scanf("%d",&a);
    printf("\n");

    printf("Enter the 2nd number : \n");
    scanf("%d",&b);
    printf("\n");

    sum = a+b;

    printf("The sum is %d. \n", sum);

    return 0;
}
